        <h1>Jak dbać o zdrowie żółwia wodnego?</h1>
        <div class="article-image floating-left">
            <img src="images/zol11.jpg" alt="" style="width: 300px">
        </div>
        <p>
            Zdrowie żółwia wodnego w dużej mierze zależy od <u>prawidłowej diety</u> oraz regularnej opieki.
        </p>
        <br>
        <p>
            Żółwie są wszystkożerne, dlatego ich dieta powinna być zróżnicowana i obejmować <i>pokarmy roślinne</i> (takie jak rośliny wodne, warzywa) oraz pokarmy zwierzęce (np. małe ryby, krewetki).
        </p>
        <br>
        <p>
            Regularnie sprawdzaj, czy żółw <b>nie wykazuje objawów chorób</b>, takich jak zmiany na skorupie, miękkość skorupy czy problemy z oczami.
        </p>
        <br>
        <p>
            Pamiętaj, że zdrowa skorupa to wynik odpowiedniej diety i <i>dostępu do światła UVB</i>.
        </p>
    </article>

    <article>
        <h1>Jak utrzymać czystość w akwarium?</h1>
        <p>
            Aby żółw mógł cieszyć się zdrowiem, kluczowa jest <b><i>czystość wody</i></b> w akwarium.
        </p>
        <br>
        <p>
            Regularna filtracja i wymiana wody (co najmniej 25% wody co tydzień) pozwalają utrzymać odpowiednią jakość wody i zapobiegają gromadzeniu się amoniaku i innych zanieczyszczeń.
        </p>
        <br>
        <p>
            Utrzymanie poziomu <u>pH wody między 6,5 a 7,5</u> oraz stała temperatura (24-28°C) także pomagają stworzyć <i>zdrowe środowisko</i> dla twojego żółwia.
        </p>
    </article>

