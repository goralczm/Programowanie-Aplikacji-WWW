            <li>Dobrze dobrany filtr nie tylko <b>zapewnia czystość</b> wody, ale również pomaga utrzymać żółwia w zdrowiu.</li>
            <li><i>Grzałka</i> i termometr pozwolą ci kontrolować temperaturę, zapewniając optymalne warunki do życia.</li>
